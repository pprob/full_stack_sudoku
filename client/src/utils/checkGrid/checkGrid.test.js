import checkGrid from './checkGrid'

describe('checkGrid', () => {
  it('returns false when grid is not full of non-zero values', () => {
    expect(
      checkGrid([
        [0, 4, 2, 6, 5, 1, 3, 9, 7],
        [5, 3, 7, 2, 8, 9, 6, 4, 1],
        [6, 9, 1, 7, 3, 4, 5, 2, 8],
        [1, 6, 3, 8, 4, 5, 9, 7, 2],
        [7, 5, 8, 1, 9, 2, 4, 6, 3],
        [9, 2, 4, 3, 7, 6, 1, 8, 5],
        [4, 7, 6, 5, 1, 8, 2, 3, 9],
        [2, 8, 5, 9, 6, 3, 7, 1, 4],
        [3, 1, 9, 3, 1, 7, 8, 5, 6]
      ])).toBeFalsy()
    expect(
      checkGrid([
        [8, 4, 2, 6, 5, 1, 3, 9, 7],
        [5, 3, 7, 2, 8, 9, 6, 4, 1],
        [6, 9, 1, 7, 3, 4, 5, 2, 8],
        [1, 6, 3, 8, 4, 5, 9, 7, 2],
        [7, 5, 8, 1, 0, 2, 4, 6, 3],
        [9, 2, 4, 3, 7, 6, 1, 8, 5],
        [4, 7, 6, 5, 1, 8, 2, 3, 9],
        [2, 8, 5, 9, 6, 3, 7, 1, 4],
        [3, 1, 9, 3, 1, 7, 8, 5, 6]
      ])).toBeFalsy()
    expect(
      checkGrid([
        [8, 4, 2, 6, 5, 1, 3, 9, 7],
        [5, 3, 7, 2, 8, 9, 6, 4, 1],
        [6, 9, 1, 7, 3, 4, 5, 2, 8],
        [1, 6, 3, 8, 4, 5, 9, 7, 2],
        [7, 5, 8, 1, 9, 0, 4, 6, 3],
        [9, 2, 4, 3, 7, 6, 1, 8, 5],
        [4, 0, 6, 5, 1, 8, 2, 3, 9],
        [2, 8, 5, 9, 6, 3, 7, 1, 4],
        [3, 1, 9, 3, 1, 7, 8, 5, 6]
      ])
    ).toBeFalsy()
    expect(
      checkGrid([[0, 5, 4, 8, 7, 3, 1, 2, 9],
      [2, 1, 9, 6, 4, 0, 8, 7, 3],
      [8, 3, 7, 5, 1, 9, 6, 0, 4],
      [5, 2, 8, 9, 6, 7, 4, 1, 0],
      [3, 7, 6, 4, 0, 5, 2, 9, 8],
      [4, 9, 0, 1, 2, 8, 5, 3, 7],
      [7, 4, 2, 0, 8, 1, 3, 0, 6],
      [1, 6, 3, 0, 0, 2, 9, 8, 5],
      [0, 8, 5, 7, 9, 6, 0, 4, 2]])
    ).toBeFalsy()
  })
  it('returns true when grid is full of non-zero values', () => {
    expect(checkGrid([
      [8, 4, 2, 6, 5, 1, 3, 9, 7],
      [5, 3, 7, 2, 8, 9, 6, 4, 1],
      [6, 9, 1, 7, 3, 4, 5, 2, 8],
      [1, 6, 3, 8, 4, 5, 9, 7, 2],
      [7, 5, 8, 1, 9, 2, 4, 6, 3],
      [9, 2, 4, 3, 7, 6, 1, 8, 5],
      [4, 7, 6, 5, 1, 8, 2, 3, 9],
      [2, 8, 5, 9, 6, 3, 7, 1, 4],
      [3, 1, 9, 3, 1, 7, 8, 5, 6]
    ])).toBeTruthy()
  })
})